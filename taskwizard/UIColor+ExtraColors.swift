import UIKit

extension UIColor {
    
    public class func buttonEnableColor() -> UIColor {
        return UIColor(red: 50 / 255.0, green: 101 / 255.0,
                       blue: 38 / 255.0, alpha: 1.0)
    }
    
    public class func buttonDisableColor() -> UIColor {
        return UIColor(red: 218 / 255.0, green: 221 / 255.0,
                       blue: 226 / 255.0, alpha: 1.0)
    }
    
}
